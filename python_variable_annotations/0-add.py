#!/usr/bin/env python3
"""
A module that provides a function to add two floats.
"""


def add(a: float, b: float) -> float:
    """
    Return the sum of two floats.
    Args:
        a (float): The first addend.
        b (float): The second addend.
    Returns:
        float: The sum of a and b.
    """
    return a + b
